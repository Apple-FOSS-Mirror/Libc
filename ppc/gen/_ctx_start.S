#include <architecture/ppc/asm_help.h>

TEXT
LABEL(__ctx_start)
	mtlr  r14
	blrl			/* branch to start function */
	mr     r3, r15		/* pass pointer to ucontext as argument */
	BRANCH_EXTERN(__ctx_done) /* branch to ctxt completion func */
	trap			/* should never get here */


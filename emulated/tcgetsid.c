/*
 * tcgetsid.c
 */
#define COMPAT_43_TTY		1
#include <sys/ioctl.h>
#include <termios.h>

pid_t
tcgetsid(int fildes)
{
	pid_t sid;

	if (ioctl(fildes, TIOCGSID, (char *)&sid) < 0)
		return ((pid_t)-1);

	return (sid);
}

